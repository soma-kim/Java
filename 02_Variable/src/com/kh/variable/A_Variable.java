package com.kh.variable;

public class A_Variable {
	
	// 변수의 필요성
	// 시급과 근무 시간과 근무 일수를 곱해서 월급을 계산하는 프로그램
	public void printSalary() {
		
		System.out.println("===== 변수 사용 전 =====");
		
		System.out.println("시급: 9160원");
		System.out.println("근무시간: 6시간");
		System.out.println("근무일수: 5일");
		
		// 월급 = 시급 x 근무시간 x 근무일수
		// 홍길동: 00000 원 형식으로 출력
		
		System.out.println("강성훈: 9160 x 6 x 5");
		System.out.println("구샛별: 9160 x 6 x 5");
		System.out.println("김도연: 9160 x 6 x 5");
		System.out.println("김수빈: 9160 x 6 x 5");
		
	}
	

}
